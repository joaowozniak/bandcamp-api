class Album:
    def __init__(self, album_id, description, title, artist, band_url, genre, genre_id, audio_url):
        self.album_id = album_id
        self.description = description
        self.title = title
        self.artist = artist
        self.band_url = band_url
        self.genre = genre
        self.genre_id = genre_id
        self.audio_url = audio_url
