class Track:
    def __init__(self, track_id, title, track_url, artist, location_text, album_id,
                 album_title, band_id, label, url):
        self.track_id = track_id
        self.title = title
        self.track_url = track_url
        self.artist = artist
        self.location_text = location_text
        self.album_id = album_id
        self.album_title = album_title
        self.band_id = band_id
        self.label = label
        self.url = url
