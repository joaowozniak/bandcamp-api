class WeeklyShow:
    def __init__(self, show_id, date, title, subtitle, description, audio_title, audio_stream, tracks):
        self.show_id = show_id
        self.date = date
        self.title = title
        self.subtitle = subtitle
        self.description = description
        self.audio_title = audio_title
        self.audio_stream = audio_stream
        self.tracks = tracks
